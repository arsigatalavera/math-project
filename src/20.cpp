#include <iostream>
using namespace std;

int main() {
    int x = 5;
    double y = 3.141592653589793;
    float z = 10.5f;
    string s = "Hello, World!";
    
    cout << "x = " << x << endl;
    cout << "y = " << y << endl;
    cout << "z = " << z << endl;
    cout << "s = " << s << endl;

    return 0;
}
